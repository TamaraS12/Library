    private void prepareTableOrder() {
        TableModelOrder tm = new TableModelOrder(new ArrayList<>());
        tblItems.setModel(tm);
    }

    public void getPublishers() {
        try {
            Controller.getInstance().getAllPublishers();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }

    public void setCmbPublisher(List<Publisher> publishers) {
        cmbPublisher.removeAllItems();
        for (Publisher publisher : publishers) {
            cmbPublisher.addItem(publisher);
        }
    }

    public void setCmbPublication(List<Publication> publications) {
        cmbPublication.removeAllItems();
        for (Publication publication : publications) {
            cmbPublication.addItem(publication);
        }
    }
