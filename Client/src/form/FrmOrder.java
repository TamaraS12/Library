    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        Publication publication = (Publication) cmbPublication.getSelectedItem();
        int quantity = Integer.valueOf(txtQuantity.getText());
        TableModelOrder tm = (TableModelOrder) tblItems.getModel();
        OrderItem orderItem = new OrderItem();
        orderItem.setPublication(publication);
        orderItem.setQuantity(quantity);
        tm.addOrderItem(orderItem);
    }//GEN-LAST:event_btnAddActionPerformed

    private void cmbPublisherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPublisherActionPerformed

        Publisher selectedPublisher = (Publisher) this.cmbPublisher.getSelectedItem();
        try {
            Controller.getInstance().getPublicationsByPublisherId(selectedPublisher.getPublisherID());
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cmbPublisherActionPerformed


